


n = int(input("Number: "))

if n>=91 and n<=100:
    print("Your Grade is A")
elif n>=81 and n<91:
    print("Your Grade is A-")
elif n>=71 and n<81:
    print("Your Grade is B")
elif n>=61 and n<71:
    print("Your Grade is B-")
elif n>=51 and n<61:
    print("Your Grade is C")
elif n>=41 and n<51:
    print("Your Grade is C-")
elif n>=33 and n<41:
    print("Your Grade is D")
elif n>=21 and n<33:
    print("Your Grade is E")
elif n>=0 and n<21:
    print("Your Grade is E-")
else:
    print("Invalid Input!")
